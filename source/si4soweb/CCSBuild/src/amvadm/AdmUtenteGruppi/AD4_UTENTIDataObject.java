//load @59-AC8CAA5B
    boolean load() {
        boolean isErrors = false;
        JDBCConnection ds = JDBCConnectionFactory.getJDBCConnection( "cn" );
        ds.setLocale(page.getCCSLocale().getLocale());
        RawCommand command = new RawCommand( ds );

        command.setSql( "SELECT u.NOMINATIVO NOMINATIVO,  "
                    + "AD4_UTENTE.GET_DATA_PASSWORD('{Utente}','Y') DATA_PASSWORD "
                    + "FROM AD4_UTENTI u "
                    + "WHERE u.UTENTE = '{IDUTE}'" );
        if ( StringUtils.isEmpty( (String) urlIDUTE.getObjectValue() ) ) urlIDUTE.setValue( "" );
        command.addParameter( "IDUTE", urlIDUTE, null );
        if ( ! command.isSetAllParams() ) {
            empty = true;
            ds.closeConnection();
            return true;
        }
        if ( ! StringUtils.isEmpty( orderBy ) ) {
            command.setOrder( orderBy );
        }

        fireBeforeBuildSelectEvent( new DataObjectEvent(command) );


        fireBeforeExecuteSelectEvent( new DataObjectEvent(command) );

        DbRow record = null;
        if ( ! ds.hasErrors() ) {
            record = command.getOneRow();
        }

        CCLogger.getInstance().debug(command.toString());

        fireAfterExecuteSelectEvent( new DataObjectEvent(command) );

        ds.closeConnection();
//End load

//loadDataBind @59-11CC5F97
        if ( record == null || record.isEmpty() ) {
            empty = true;
        } else {
            row.setNOMINATIVO(Utils.convertToString(ds.parse(record.get("NOMINATIVO"), row.getNOMINATIVOField())));
            row.setDATA_PASSWORD(Utils.convertToString(ds.parse(record.get("DATA_PASSWORD"), row.getDATA_PASSWORDField())));
        }
//End loadDataBind

//End of load @59-B1C63002
        isErrors = ds.hasErrors();
        if ( isErrors ) addErrors( ds.getErrors() );
        return ( ! isErrors );
    }
//End End of load

