//load @6-C29A2657
    boolean load() {
        boolean isErrors = false;
        JDBCConnection ds = JDBCConnectionFactory.getJDBCConnection( "cn" );
        ds.setLocale(page.getCCSLocale().getLocale());
        RawCommand command = new RawCommand( ds );

        command.setSql( "select NOMINATIVO "
                    + ", UTENTE "
                    + ", '(Id.: '||to_char(ID_UTENTE)||')' DSP_ID_UTENTE "
                    + ",  "
                    + "ULTIMO_TENTATIVO "
                    + ", decode(nvl(NUMERO_TENTATIVI,0) "
                    + "        ,0,'' "
                    + "          ,  "
                    + "' (Tentativi falliti: '||NUMERO_TENTATIVI||')' "
                    + "        ) DSP_NUMERO_TENTATIVI "
                    + ",  "
                    + "DATA_INSERIMENTO "
                    + ", DATA_AGGIORNAMENTO "
                    + ",  "
                    + "UTENTE_AGGIORNAMENTO "
                    + "from AD4_UTENTI "
                    + "where UTENTE = '{Utente}'" );
        if ( StringUtils.isEmpty( (String) sesUtente.getObjectValue() ) ) sesUtente.setValue( "" );
        command.addParameter( "Utente", sesUtente, null );
        if ( ! command.isSetAllParams() ) {
            empty = true;
            ds.closeConnection();
            return true;
        }
        if ( ! StringUtils.isEmpty( orderBy ) ) {
            command.setOrder( orderBy );
        }

        fireBeforeBuildSelectEvent( new DataObjectEvent(command) );


        fireBeforeExecuteSelectEvent( new DataObjectEvent(command) );

        DbRow record = null;
        if ( ! ds.hasErrors() ) {
            record = command.getOneRow();
        }

        CCLogger.getInstance().debug(command.toString());

        fireAfterExecuteSelectEvent( new DataObjectEvent(command) );

        ds.closeConnection();
//End load

//loadDataBind @6-1DD94ACF
        if ( record == null || record.isEmpty() ) {
            empty = true;
        } else {
            row.setNOMINATIVO(Utils.convertToString(ds.parse(record.get("NOMINATIVO"), row.getNOMINATIVOField())));
            row.setUTENTE(Utils.convertToString(ds.parse(record.get("UTENTE"), row.getUTENTEField())));
            row.setDSP_ID_UTENTE(Utils.convertToString(ds.parse(record.get("DSP_ID_UTENTE"), row.getDSP_ID_UTENTEField())));
            try {
                row.setULTIMO_TENTATIVO(Utils.convertToDate(ds.parse(record.get("ULTIMO_TENTATIVO"), row.getULTIMO_TENTATIVOField())));
            } catch ( java.text.ParseException pe ) {
                model.addError( "Invalid data" );
            }
            row.setDSP_NUMERO_TENTATIVI(Utils.convertToString(ds.parse(record.get("DSP_NUMERO_TENTATIVI"), row.getDSP_NUMERO_TENTATIVIField())));
            try {
                row.setDATA_INSERIMENTO(Utils.convertToDate(ds.parse(record.get("DATA_INSERIMENTO"), row.getDATA_INSERIMENTOField())));
            } catch ( java.text.ParseException pe ) {
                model.addError( "Invalid data" );
            }
            try {
                row.setDATA_AGGIORNAMENTO(Utils.convertToDate(ds.parse(record.get("DATA_AGGIORNAMENTO"), row.getDATA_AGGIORNAMENTOField())));
            } catch ( java.text.ParseException pe ) {
                model.addError( "Invalid data" );
            }
            row.setUTENTE_AGGIORNAMENTO(Utils.convertToString(ds.parse(record.get("UTENTE_AGGIORNAMENTO"), row.getUTENTE_AGGIORNAMENTOField())));
        }
//End loadDataBind

//End of load @6-B1C63002
        isErrors = ds.hasErrors();
        if ( isErrors ) addErrors( ds.getErrors() );
        return ( ! isErrors );
    }
//End End of load

