//load @6-CD1044A4
    boolean load() {
        boolean isErrors = false;
        JDBCConnection ds = JDBCConnectionFactory.getJDBCConnection( "cn" );
        ds.setLocale(page.getCCSLocale().getLocale());
        RawCommand command = new RawCommand( ds );

        command.setSql( "SELECT u.NOMINATIVO NOMINATIVO "
                    + ",  "
                    + "AD4_UTENTE.GET_DATA_PASSWORD('{Utente}','Y') DATA_PASSWORD "
                    + ",  "
                    + "nvl(AD4_UTENTE.GET_RINNOVO_PASSWORD('{Utente}'),'SI') RINNOVO_PASSWORD "
                    + ",  "
                    + "amvweb.get_preferenza('Rinnovo Password Utenti','{Modulo}') RINNOVO_PASSWORD_PORTALE "
                    + ", r.DESCRIZIONE RUOLO "
                    + "FROM AD4_UTENTI u,  "
                    + "AD4_RUOLI r  "
                    + "WHERE u.UTENTE = '{Utente}' "
                    + "and r.RUOLO = '{Ruolo}'" );
        if ( StringUtils.isEmpty( (String) sesUtente.getObjectValue() ) ) sesUtente.setValue( "" );
        command.addParameter( "Utente", sesUtente, null );
        if ( StringUtils.isEmpty( (String) sesRuolo.getObjectValue() ) ) sesRuolo.setValue( "" );
        command.addParameter( "Ruolo", sesRuolo, null );
        if ( StringUtils.isEmpty( (String) sesMVUTE.getObjectValue() ) ) sesMVUTE.setValue( "" );
        command.addParameter( "MVUTE", sesMVUTE, null );
        if ( StringUtils.isEmpty( (String) sesModulo.getObjectValue() ) ) sesModulo.setValue( "" );
        command.addParameter( "Modulo", sesModulo, null );
        if ( ! command.isSetAllParams() ) {
            empty = true;
            ds.closeConnection();
            return true;
        }
        if ( ! StringUtils.isEmpty( orderBy ) ) {
            command.setOrder( orderBy );
        }

        fireBeforeBuildSelectEvent( new DataObjectEvent(command) );


        fireBeforeExecuteSelectEvent( new DataObjectEvent(command) );

        DbRow record = null;
        if ( ! ds.hasErrors() ) {
            record = command.getOneRow();
        }

        CCLogger.getInstance().debug(command.toString());

        fireAfterExecuteSelectEvent( new DataObjectEvent(command) );

        ds.closeConnection();
//End load

//loadDataBind @6-1127BC0C
        if ( record == null || record.isEmpty() ) {
            empty = true;
        } else {
            row.setNOMINATIVO(Utils.convertToString(ds.parse(record.get("NOMINATIVO"), row.getNOMINATIVOField())));
            row.setRUOLO(Utils.convertToString(ds.parse(record.get("RUOLO"), row.getRUOLOField())));
            row.setDATA_PASSWORD(Utils.convertToString(ds.parse(record.get("DATA_PASSWORD"), row.getDATA_PASSWORDField())));
            row.setRINNOVO_PASSWORD_PORTALE(Utils.convertToString(ds.parse(record.get("RINNOVO_PASSWORD_PORTALE"), row.getRINNOVO_PASSWORD_PORTALEField())));
            row.setRINNOVO_PASSWORD(Utils.convertToString(ds.parse(record.get("RINNOVO_PASSWORD"), row.getRINNOVO_PASSWORDField())));
        }
//End loadDataBind

//End of load @6-B1C63002
        isErrors = ds.hasErrors();
        if ( isErrors ) addErrors( ds.getErrors() );
        return ( ! isErrors );
    }
//End End of load

