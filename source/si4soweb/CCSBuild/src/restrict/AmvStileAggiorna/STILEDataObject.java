    boolean load() {
        boolean isErrors = false;
        JDBCConnection ds = JDBCConnectionFactory.getJDBCConnection( "cn" );
        ds.setLocale(page.getCCSLocale().getLocale());
        RawCommand command = new RawCommand( ds );

        command.setSql( "select '{Utente}' P_UTENTE, 'Style' P_STRINGA, upper('{Modulo}') P_MODULO,  "
                    + "'{STYLESHEET}' P_STYLESHEET from dual" );
        if ( StringUtils.isEmpty( (String) sesUtente.getObjectValue() ) ) sesUtente.setValue( "" );
        command.addParameter( "Utente", sesUtente, null );
        if ( StringUtils.isEmpty( (String) sesModulo.getObjectValue() ) ) sesModulo.setValue( "" );
        command.addParameter( "Modulo", sesModulo, null );
        if ( StringUtils.isEmpty( (String) urlSTYLESHEET.getObjectValue() ) ) urlSTYLESHEET.setValue( "" );
        command.addParameter( "STYLESHEET", urlSTYLESHEET, null );
        if ( ! command.isSetAllParams() ) {
            empty = true;
            ds.closeConnection();
            return true;
        }
        if ( ! StringUtils.isEmpty( orderBy ) ) {
            command.setOrder( orderBy );
        }

        fireBeforeBuildSelectEvent( new DataObjectEvent(command) );


        fireBeforeExecuteSelectEvent( new DataObjectEvent(command) );

        DbRow record = null;
        if ( ! ds.hasErrors() ) {
            record = command.getOneRow();
        }

        CCLogger.getInstance().debug(command.toString());

        fireAfterExecuteSelectEvent( new DataObjectEvent(command) );

        ds.closeConnection();
//End load

//loadDataBind @2-DB4B9D72
        if ( record == null || record.isEmpty() ) {
            empty = true;
        } else {
            row.setP_UTENTE(Utils.convertToString(ds.parse(record.get("P_UTENTE"), row.getP_UTENTEField())));
            row.setP_STRINGA(Utils.convertToString(ds.parse(record.get("P_STRINGA"), row.getP_STRINGAField())));
            row.setP_MODULO(Utils.convertToString(ds.parse(record.get("P_MODULO"), row.getP_MODULOField())));
            row.setP_STYLESHEET(Utils.convertToString(ds.parse(record.get("P_STYLESHEET"), row.getP_STYLESHEETField())));
        }
//End loadDataBind

//End of load @2-B1C63002
        isErrors = ds.hasErrors();
        if ( isErrors ) addErrors( ds.getErrors() );
        return ( ! isErrors );
    }
//End End of load

//update @2-7E48AD26
        boolean update() {
            boolean isErrors = false;
            JDBCConnection ds = JDBCConnectionFactory.getJDBCConnection( "cn" );
            ds.setLocale(page.getCCSLocale().getLocale());
            SPCommand command = SPCommandFactory.getSPCommand( "cn" );
            command.setJdbcConnection( ds );

            command.setSql( "{ call AMVWEB.SET_PREFERENZA ( ?, ?, ?, ? ) }" );
            if ( StringUtils.isEmpty( (String) exprKey17.getObjectValue() ) ) exprKey17.setValue( "" );
            command.addParameter( "P_STRINGA", exprKey17, java.sql.Types.CHAR, 0, SPParameter.INPUT_PARAMETER );
            if ( StringUtils.isEmpty( (String) urlSTYLESHEET.getObjectValue() ) ) urlSTYLESHEET.setValue( "" );
            command.addParameter( "P_VALORE", urlSTYLESHEET, java.sql.Types.CHAR, 0, SPParameter.INPUT_PARAMETER );
            if ( StringUtils.isEmpty( (String) sesModulo.getObjectValue() ) ) sesModulo.setValue( "" );
            command.addParameter( "P_MODULO", sesModulo, java.sql.Types.CHAR, 0, SPParameter.INPUT_PARAMETER );
            if ( StringUtils.isEmpty( (String) sesUtente.getObjectValue() ) ) sesUtente.setValue( "" );
            command.addParameter( "P_UTENTE", sesUtente, java.sql.Types.CHAR, 0, SPParameter.INPUT_PARAMETER );

            fireBeforeBuildUpdateEvent( new DataObjectEvent(command) );


//End update

//updateDataBound @2-0130DCE2
            fireBeforeExecuteUpdateEvent( new DataObjectEvent(command) );

            if ( ! ds.hasErrors() ) {
                command.executeUpdate();
            }

            CCLogger.getInstance().debug(command.toString());

            fireAfterExecuteUpdateEvent( new DataObjectEvent(command) );

//End updateDataBound

//End of update @2-CDC5319F
            ds.closeConnection();
            isErrors = ds.hasErrors();
            if ( isErrors ) addErrors( ds.getErrors() );
            return ( ! isErrors );
        }
//End End of update

